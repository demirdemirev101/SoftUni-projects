function loadingBar(number){
    switch(number){
        case number=10:
            console.log("10% [%.........]");
            console.log("Still loading...");
        break;
        case number=20:
            console.log("20% [%.........]");
            console.log("Still loading...");
        break;
        case number=30:
            console.log("30% [%%%.......]");
            console.log("Still loading...");
        break;
        case number=40:
            console.log("40% [%%%%......]");
            console.log("Still loading...");
        break;
        case number=50:
            console.log("50% [%%%%%.....]");
            console.log("Still loading...");
        break;
        case number=60:
            console.log("60% [%%%%%%....]");
            console.log("Still loading...");
        break;
        case number=70:
            console.log("70% [%%%%%%%...]");
            console.log("Still loading...");
        break;
        case number=80:
            console.log("80% [%%%%%%%%..]");
            console.log("Still loading...");
        break;
        case number=90:
            console.log("90% [%%%%%%%%%.]");
            console.log("Still loading...");
        break;
        case number=100:
            console.log("100% Complete!");
            console.log("[%%%%%%%%%%]");
        break;
    }
}
loadingBar(10);
